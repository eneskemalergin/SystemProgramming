
	/* Demonstrates 3 different styles for indenting code (pgs 197-198) */

	/* Indenting brackets to match code block */
i=0;
while (i < 3)
  {
  printf("Hello turtle %d\n",i);
  i++;
  }
printf("How are you?\n");


	/* Indenting code block only */
i=0;
while (i < 3)
{
  printf("Hello turtle %d\n",i);
  i++;
}
printf("How are you?\n");


	/* Concatenating brace(s) to compress code vertically */
i=0;
while (i < 3) {
  printf("Hello turtle %d\n",i);
  i++;
  }
printf("How are you?\n");

